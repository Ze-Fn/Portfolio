def area_rect():
    '''
    This program calculates the area of a rectangular of any given side measurement (cm).
    '''
    x = float(input("Insert the width: "))
    y = float(input("Insert the length: "))
    return x * y

print(f"The area of your rectangle is {area_rect()} cm^2")

